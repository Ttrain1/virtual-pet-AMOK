package virtualpetAMOK;

public class Pet {

	String name;
	String description;
	int happiness;
	int health;
	boolean alive;

	public String getName() {
		return name;
	}

	public String getDescription() {
		return description;
	}

	public int getHappiness() {
		return happiness;
	}

	public int getHealth() {
		return health;
	}

	public boolean isAlive() {
		return alive;
	}



	public Pet() {
		super();
	}

}